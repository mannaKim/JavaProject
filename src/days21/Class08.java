package days21;

// Getter 와 Setter 만들기

class Student {
    int bunho;
    String name;
    int kor;
    int eng;
    int mat;
    int tot;
    double avg;

    public int getBunho() {
        return bunho;
    }
    public void setBunho(int bunho) {
        this.bunho = bunho;
    }

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }

    public int getKor() {
        return kor;
    }
    public void setKor(int kor) {
        this.kor = kor;
    }

    public int getEng() {
        return eng;
    }
    public void setEng(int eng) {
        this.eng = eng;
    }

    public int getMat() {
        return mat;
    }
    public void setMat(int mat) {
        this.mat = mat;
    }

    public int getTot() {
        return tot;
    }
    public void setTot(int tot) {
        this.tot = tot;
    }

    public double getAvg() {
        return avg;
    }
    public void setAvg(double avg) {
        this.avg = avg;
    }
}

